fun main() {
    val numbers = arrayOf(10, 20, 30, 40)
    val penjumlahan = numbers[0] + numbers[2]
    val perkalian = numbers[1] * numbers[3]

    println("Variable numbers memiliki ${numbers.size} buah data")
    println("Jika data ke 1 dan ke 3 di tambahkan maka hasilnya adalah $penjumlahan")
    println("Jika data ke 2 dan ke 4 di kalikan maka hasilnya adalah $perkalian")
}
